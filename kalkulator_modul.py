#kalkulator modul 

#fungsi tambah 
def tambah (a,b):
    return a+b

#fungsi kurang
def kurang (a,b):
    return a-b

#fungsi kali
def kali (a,b):
    return a*b

#fungsi bagi
def bagi (a,b):
    if b != 0:
        return a/b
    else: 
        return "Errorpembagi tidak boleh nol"

# fungsi modulus 
def modulus (a, b):
    return a % b

# fungsi pangkat 
def pangkat (a,b):
    return a**b



     



